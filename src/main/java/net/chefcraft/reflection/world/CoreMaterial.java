	static {
		for (CoreMaterial cm : VALUES) {
			cm.asBukkitMaterial = matchMaterial(cm, true);
			cm.legacyItemSuffix = hasLegacyItemSuffix(cm);
			
			CoreMaterial stateToBlock = blockStateToItem(cm);
			cm.blockState = stateToBlock != cm;
			cm.asBukkitItemStack = matchItemStack(stateToBlock, true);
		}
	}
	
