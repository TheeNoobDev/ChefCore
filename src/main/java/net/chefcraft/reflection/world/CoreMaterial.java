	@Nullable
	public static CoreMaterial toCore(Material material) {
		for (int i = 0; i < VALUES.length; i++) {
			if (VALUES[i].asBukkitMaterial == material) {
				return VALUES[i];
			}
		}
		return null;
	}
	
	/**
	 * @param name name of material
	 * @returns if matches, matched value or else "STONE"
	 */
	@NotNull
	public static CoreMaterial matchByName(@NotNull String name) {
		return matchByName(name, true);
	}
	
	/**
	 * @param name name of material
	 * @param logReturns sends a message to the console when the value does not match
	 * @returns if matches, matched value or else "STONE"
	 */
	@NotNull
	public static CoreMaterial matchByName(@NotNull String name, boolean logReturns) {
		CoreMaterial match = getByName(name);
		
		if (match == null && logReturns) {
			
			Bukkit.getServer().getLogger()
			.log(Level.SEVERE, "[Core Material] No match found for \"" + name + "\" core material! Returning as \"STONE\"");
			
			return CoreMaterial.STONE;
		}
		
		return match;
	}
	
	@NotNull
	private static Material matchMaterial(@NotNull CoreMaterial nnmaterial, boolean logReturns) {
		Material match = BukkitUtils.MATERIAL_MAPPINGS.get(nnmaterial.name());
		if (match == null) {
			boolean log = nnmaterial.customTranslation;
			if (nnmaterial.reflectedValues != null) {
				for (String oValue : nnmaterial.reflectedValues) {
					match = BukkitUtils.MATERIAL_MAPPINGS.get(oValue);
					if (match == null) continue;
					
					if (log && logReturns) {
						
						Bukkit.getServer().getLogger()
						.log(Level.WARNING, "[Core Material] Match found for \"" + nnmaterial + "\" material! Return Value: \"" + oValue + "\"");
					}
					
					return match;
				}
			}
			
			if (match == null && nnmaterial.linkedValue != null) {
				return matchMaterial(nnmaterial.linkedValue, logReturns);
			} else {
				
				Bukkit.getServer().getLogger()
				.log(Level.SEVERE, "[Core Material] No match found for \"" + nnmaterial + "\" material! Returning as \"STONE\"");
				
				return Material.STONE;
			}
		}
		return match;
	}
	
	@NotNull
	private static ItemStack matchItemStack(@NotNull CoreMaterial coreMaterial, boolean logReturns) {
		Material match = matchMaterial(coreMaterial, logReturns);
		try {
			if (ServerVersion.isLegacy()) {
				return new ItemStack(coreMaterial.legacyItemSuffix ? BukkitUtils.MATERIAL_MAPPINGS.get(match.name() + "_ITEM") : match, 1, (byte) coreMaterial.legacyData);
			} else {
				return new ItemStack(match, 1);
			}
		} catch (Exception x) {
			
			Bukkit.getServer().getLogger()
			.log(Level.SEVERE, "[Core Material] No match found for \"" + coreMaterial + "\" item! Returning as \"STONE\"");
			
			return new ItemStack(Material.STONE);
		}
	}
	
